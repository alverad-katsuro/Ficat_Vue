        Para começar a usar o sistema e gerar uma ficha, é necessário ir até a
        página inicial do site disponível em
        <a><strong>bcficat.ufpa.br</strong></a> ou clique no ícone do FICAT no
        <strong>menu de navegação</strong> no topo dessa página.
      </p>

      <h5>Dados do autor</h5>
      <p>
        Nessa primeira seção do formulário, você deve adicionar as informações
        relacionadas ao autor ou autores. Caso queira adicionar mais de um
        autor, pressione o <strong>botão de adicionar</strong> ao lado dos
        campos de nome e sobrenome. Atualmente o FICAT aceita apenas dois
        autores por trabalho.
      </p>
      <img
        src="../assets/img/dados_autor.png"
        alt="Imagem mostrando os dados do Autor que devem ser preenchidos."
      />
      <p>
        Após o preenchimento dos campos, clique em
        <strong>Próximo</strong>.
      </p>

      <h5>Dados do trabalho</h5>
      <p>
        Nessa seção você deve preencher os campos com as informações
        relacionadas ao trabalho que necessita da ficha catalográfica. Comece
        preenchendo a primeira coluna, informando o titulo do trabalho e o
        subtitulo, se houver. Depois selecione o ano em que o trabalho foi
        finalizado e o total de paginas.
      </p>
      <p>
        Caso a numeração tenha apenas números arábicos, preencha normalmente com
        o número, agora se tiver tanto números arábicos quanto romanos, preencha
        de acordo com o exemplo no campo, atentando ao fato de que
        <strong>os números romanos devem ser em letras minusculas</strong>.
      </p>
      <img
        src="../assets/img/dados_trabalho.png"
        alt="Imagem mostrandos os dados do trabalho que devem ser preenchidos."
      />
      <p>
        No inicio da segunda coluna, escolha se o trabalho possui ilustração e o
        tipo dele (trabalho), se é tese, dissertação ou monografia. Depois
        clique no campo de área de conhecimento,
        <strong>isso abrirá uma caixa de dialogo</strong> para você escolher a
        opção mais adequada para o seu trabalho.
      </p>
      <p>
        Na caixa de dialógo de áreas do conhecimento, você tem duas opções de
        navegação: através da escolha de uma das categorias ou através de
        pesquisa. Ao clicar em uma categoria ou fazer uma pesquisa, uma lista de
        opções será exibida, clique na sua escolha e
        <strong
          >o campo de área de conhecimento será preenchido automaticamente </strong
        >.
